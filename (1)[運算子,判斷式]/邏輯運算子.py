'''
邏輯運算子
and  所有條件都必須成立 >> True
or   只要有一個條件成立 >> True
not  相反(Ex: not(False)==True  ) 
'''

print((1 > 2) and (3 > 2)) #False
print((1 > 2) or (3 > 2))  #True
print(not(1 > 2))   #True

